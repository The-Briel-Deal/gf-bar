fn print_canvas(canvas: &[u8], height: usize, width: usize) -> String {
    let mut output = String::from("");
    for i_h in 0..height {
        let start_index = i_h * width * 4;
        canvas[start_index..start_index + (width * 4)]
            .chunks_exact(4)
            .for_each(|chunk| {
                let b = chunk[0];
                let g = chunk[1];
                let r = chunk[2];
                let _a = chunk[3];
                output.push_str(&format!("{}", " ".on_truecolor(r, g, b)));
            });
        output.push('\n');
    }

    output
}
#[test]
fn test_write_text() {
    let height = 40;
    let width = 100;
    let text = "hi";
    let mut canvas_buffer = vec![];
    for _i in 0..(4 * height * width) {
        canvas_buffer.push(0x00u8);
    }
    let canvas = Canvas::new(&mut canvas_buffer, width, height);
    write_text(canvas, text, cosmic_text::Align::Left);
    println!("{canvas_buffer:#X?}");

    assert!(
        canvas_buffer == TEST_WRITE_TEXT_EXPECTED,
        "expected: \n{}\n recieved: \n{}\n ",
        print_canvas(TEST_WRITE_TEXT_EXPECTED, height as usize, width as usize),
        print_canvas(&canvas_buffer, height as usize, width as usize)
    );
}
