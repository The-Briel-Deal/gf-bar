impl SimpleLayer {
    pub fn get_width(&mut self) -> i32 {
        let first_output = self.output_state().outputs().next().unwrap();
        let first_output_info = self.output_state().info(&first_output).unwrap();
        let logical_size = first_output_info.logical_size.as_ref().unwrap();
        logical_size.0
    }
    pub fn draw(&mut self, qh: &QueueHandle<Self>) {
        let width = self.width;
        let height = self.height;
        let stride = self.width as i32 * 4;

        let (buffer, canvas) = self
            .pool
            .create_buffer(
                width as i32,
                height as i32,
                stride,
                wl_shm::Format::Argb8888,
            )
            .expect("create buffer");

        // Draw to the window:
        {
            let shift = self.shift.unwrap_or(0);
            canvas
                .chunks_exact_mut(4)
                .enumerate()
                .for_each(|(index, chunk)| {
                    let _x = ((index + shift as usize) % width as usize) as u32;
                    let _y = (index / width as usize) as u32;

                    // Tokyo Night Background Color '#1a1b26'
                    let color = Color::rgb(0x1a, 0x1b, 0x26);

                    let array: &mut [u8; 4] = chunk.try_into().unwrap();
                    *array = [color.b(), color.g(), color.r(), color.a()]; // Little Endian
                });

            if let Some(shift) = &mut self.shift {
                *shift = (*shift + 1) % width;
            }
        }

        write_text(canvas, "hi, I'm a dog", width, height);

        // Damage the entire window
        self.layer
            .wl_surface()
            .damage_buffer(0, 0, width as i32, height as i32);

        // Request our next frame
        self.layer
            .wl_surface()
            .frame(qh, self.layer.wl_surface().clone());

        // Attach and commit to present.
        buffer
            .attach_to(self.layer.wl_surface())
            .expect("buffer attach");
        self.layer.set_size(self.width, self.height);
        self.layer.commit();

        // TODO save and reuse buffer when the window size is unchanged.  This is especially
        // useful if you do damage tracking, since you don't need to redraw the undamaged parts
        // of the canvas.
    }
}

fn write_text(canvas: &mut [u8], text: &str, width: u32, height: u32) {
    const TEXT_COLOR: Color = Color::rgb(0xFF, 0xFF, 0xFF);
    let mut font_system = FontSystem::new();
    let mut swash_cache = SwashCache::new();

    let font_size: f32 = height as f32;
    let line_height: f32 = font_size * 1.2;

    let metrics = Metrics::new(font_size, line_height);
    let mut buffer = Buffer::new(&mut font_system, metrics);

    let mut buffer = buffer.borrow_with(&mut font_system);

    buffer.set_size(Some(width as f32), Some(height as f32));

    let attrs = Attrs::new();

    buffer.set_text(text, attrs, Shaping::Advanced);
    buffer.shape_until_scroll(true);

    buffer.draw(&mut swash_cache, TEXT_COLOR, |x, y, w, h, color| {
        let a = color.a();
        if a == 0 || x < 0 || x >= width as i32 || y < 0 || y >= height as i32 || w != 1 || h != 1 {
            return;
        }

        let width = width as i32 * 4;
        let line = width * y;
        let chunk_start = (x * 4) + line;
        if chunk_start as usize > canvas.len() - 4 {
            return;
        }
        let slice: &mut [u8] = &mut canvas[chunk_start as usize..(chunk_start + 4) as usize];

        // Alpha Blending
        let text_a = color.a();
        let mut text_r = color.r();
        let mut text_g = color.g();
        let mut text_b = color.b();

        let bg_r = slice[2];
        let bg_g = slice[1];
        let bg_b = slice[0];

        let alpha_percent = text_a as f32 / 255.0;

        text_r = ((bg_r as f32 * (1.0 - alpha_percent))
            + (text_r as f32 * alpha_percent).clamp(0.0, 255.0)) as u8;
        text_g = ((bg_g as f32 * (1.0 - alpha_percent))
            + (text_g as f32 * alpha_percent).clamp(0.0, 255.0)) as u8;
        text_b = ((bg_b as f32 * (1.0 - alpha_percent))
            + (text_b as f32 * alpha_percent).clamp(0.0, 255.0)) as u8;

        slice[2] = text_r;
        slice[1] = text_g;
        slice[0] = text_b;
    });
}

